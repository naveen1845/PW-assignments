// In JavaScript, conditional statements allow you to control the flow of your program based on certain conditions. There are two main types: if statements and if-else statements.

/*
If statement

syntax: 
if (condition) {
    // Code to execute if the condition is true
} 

*/

//Example
let x = 10;

if (x > 5) {
    console.log("x is greater than 5");
}




/* 
If - else statement

syntax: 
if (condition) {
    // Code to execute if the condition is true
} else {
    // Code to execute if the condition is false
}
*/


//example
let y = 3;

if (y % 2 === 0) {
    console.log("y is an even number");
} else {
    console.log("y is an odd number");
}